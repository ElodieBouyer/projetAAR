package valueObjects;

public class UserVO {

	private Integer idUser;
	private String name;
	private String login;
	private String role;

	public Integer getIdUser() {
		return idUser;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setIdUser(int id) {
		this.idUser = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the login
	 */
	public String getLogin() {
		return login;
	}

	/**
	 * @param login
	 *            the login to set
	 */
	public void setLogin(String login) {
		this.login = login;
	}

	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}

	/**
	 * @param role
	 *            the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}

}
